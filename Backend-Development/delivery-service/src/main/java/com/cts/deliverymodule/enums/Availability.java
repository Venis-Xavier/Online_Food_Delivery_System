package com.cts.deliverymodule.enums;

/**
 * Represents the availablity of the delivery agent.
 * This enum is used to track the agent's current availability status.
 * 
 * @author Jeswin Joseph J
 * @since 22 Feb 2025
 */
public enum Availability {
    
    /**
     * The delivery agent is available for delivery.
     */
    AVAILABLE,

    /**
     * The delivery agent is assigned with a delivery and currently not available.
     */
    UNAVAILABLE
}
