package com.cts.paymentservice.enums;

/**
 * Enumeration for different payment statuses.
 * This enum defines the possible statuses of a payment in the online food delivery system.
 * 
 * @author Venkata Ramakrishna Kambhampati
 * @since 25 Feb 2025
 */
public enum PaymentStatus {
    
    /**
     * Payment is pending and yet to be completed.
     */
    PENDING,
    
    /**
     * Payment has been successfully completed.
     */
    COMPLETED
}
